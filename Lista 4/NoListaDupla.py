class NoListaDupla:
    def __init__(self, info, ant, prox):
        self.info = info
        self.ant = ant
        self.prox = prox
    
    def setInfo(self, info):
        self.info = info
    
    def getInfo(self):
        return self.info
    
    def setAnt(self, ant):
        self.ant = ant
    
    def getAnt(self):
        return self.ant
    
    def setProx(self, prox):
        self.prox = prox
    
    def getProx(self):
        return self.prox